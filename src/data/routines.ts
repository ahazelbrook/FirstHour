export const routines: Routine[] = [dailyTen, extendedTwenty];

export function getRoutine(id: Routine['id']): Routine {
  const routine = routines.find((r) => r.id === id);
  if (!routine) throw new Error(`Unknown routine: ${id}`);
  return routine;
}

export function allSegments(routine: Routine) {
  return routine.blocks.flatMap((block) => block.segments);
}
